function obj = readSetupSheet(obj, filename)

    arguments (Input)
        obj (1,1) setup.Setup
        filename (1,1) string
    end

    data = readcell(filename);

    %Info
    obj.Info = setup.Info( ...
        Engineer = data{5,2}, ...
        Mechanic=data{6,2}, ...
        IssueNo=data{5,11}, ...
        DateIssued=data{5,16});

    %Driver
    obj.Driver = setup.Driver( ...
        DriverName=data{9,3}, ...
        DriverWeight=data{9,5});

    %Race information
    obj.RaceInfo = setup.RaceInformation( ...
        Circuit=data{8,10}, ...
