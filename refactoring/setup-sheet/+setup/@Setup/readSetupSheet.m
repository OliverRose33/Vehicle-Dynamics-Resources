    obj.Tires(2)=setup.TireSetup(ToeAngle=data{22,16}, ...
        ToeMMSHM=data{22,18}, ...
        CamberAngle=data{23,16}, ...
        CamberMMSHM=data{23,18}, ...
        HardpointUF=[data{25,16},data{26,16}], ...
        HardpointUR=[data{25,17},data{26,16}], ...
        HardpointLF=[data{25,18},data{26,18}], ...
        HardpointLR=[data{25,19},data{26,18}], ... 
        RideHeightAngle=data{27,16}, ...
        RideHeightMMSHM=data{27,18}, ...
        TempID=data{29,16}, ...
        TempCold=data{29,17}, ...
        TempHot=data{29,18}, ...
        Temperature=data{29,19}, ...
        CornerWeight=data{30,16});

    %Rear Left
    obj.Tires(3)=setup.TireSetup(ToeAngle=data{40,3}, ...
        ToeMMSHM=data{40,5}, ...
        CamberAngle=data{41,3}, ...
        CamberMMSHM=data{41,5}, ...
        HardpointUF=[data{43,3},data{44,3}], ...
        HardpointUR=[data{43,4},data{44,3}], ...
        HardpointLF=[data{43,5},data{44,3}], ...
