        Diff=data{11,3}, ...
        Ramps=data{12,3},... 
        PreLoad=data{13,3});

    %Brakes
    obj.Brakes=setup.Brakes( ...
        FrontMC=data{12,9}, ...
        RearMC=data{12,11}, ...
        FrontPads=data{13,9}, ...
        RearPads=data{13,11});

    %ACCU,Cooling,Rack
    obj.AccuConfig=data{11,16};
    obj.Cooling=data{12,16};
    obj.SteeringRack=data{13,16};

    %Gear Ratios
    obj.GearRatios=setup.GearRatios(...
        FrontSprocketToothCount=data{15,4}, ...
        RearSprocketToothCount=data{15,5}, ...
        FDR=data{15,6});

    %Aero
    obj.Aero=setup.Aero( ...
        FrontWing=data{18,8}, ...
        SideWing=data{18,10}, ...
        RearWing=data{18,12});

    %Tires

    %Front Left
    obj.Tires(1)=setup.TireSetup(ToeAngle=data{22,3}, ...
    ToeMMSHM=data{22,5}, ...
    CamberAngle=data{23,3}, ...
    CamberMMSHM=data{23,5}, ...
    HardpointUF=[data{25,3},data{26,3}], ...
    HardpointUR=[data{25,4},data{26,3}], ...
    HardpointLF=[data{25,5},data{26,5}], ...
    HardpointLR=[data{25,6},data{26,5}], ... 
    RideHeightAngle=data{27,3}, ...
    RideHeightMMSHM=data{27,5}, ...
    TempID=data{29,3}, ...
    TempCold=data{29,4}, ...
    TempHot=data{29,5}, ...
    Temperature=data{29,6}, ...
    CornerWeight=data{29,6});

